package main

import "fmt"

func main() {
	// addition
	fmt.Println(50+25, "  -> addition")

	// substraction
	fmt.Println(50-15.5, "-> substraction")

	// multiplication
	fmt.Println(50*0.5, "  -> multiplcation")

	// division
	fmt.Println(50/0.5, " -> division")

	// module
	fmt.Println(25%3, "   -> module")

	// increment
	i := 5
	i++
	fmt.Println(i, "   -> increment")

	// decrement
	i = 5
	i--
	fmt.Println(i, "   -> decrement")
}
